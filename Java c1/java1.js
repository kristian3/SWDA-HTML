function f2c(f){
    //console.log((f - 32) * 5/9);
    return (f - 32) * 5/9;
}

function c2f(c){
    //console.log(c * 9/5 + 32);
    return c * 9/5 + 32;
}

var c = f2c(100); // fahrenheit -> celsius
console.log(c)
var f =  c2f(28); // celsius -> fahrenheit
console.log(f);





